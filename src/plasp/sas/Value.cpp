const Value Value::Any = {Value::Sign::Positive, "(any)"};

////////////////////////////////////////////////////////////////////////////////////////////////////

std::ostream &operator <<(std::ostream &ostream, const Value &value)
{
	if (value.sign == Value::Sign::Negative)
		ostream << "not ";

	return (ostream << value.name);
}

