#ifndef __PLASP__PDDL__IDENTIFIER_H
#define __PLASP__PDDL__IDENTIFIER_H

#include <cctype>

namespace plasp
{
namespace pddl
{

////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Identifier
//
////////////////////////////////////////////////////////////////////////////////////////////////////

const auto isIdentifier =
	[](const auto character)
	{
		return character != '?'
			&& character != '('
			&& character != ')'
			&& character != ';'
			&& std::isgraph(character);
	};

////////////////////////////////////////////////////////////////////////////////////////////////////

}
}

#endif
